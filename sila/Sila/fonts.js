// Function to convert text using selected font
function convertToFont(text, fontNumber = 1) {
    if (!text) return '';
    
    let fontMap;
    switch(fontNumber) {
        case 1: fontMap = fontMap1; break;
        case 2: fontMap = fontMap2; break;
        case 3: fontMap = fontMap3; break;
        case 4: fontMap = fontMap4; break;
        case 5: fontMap = fontMap5; break;
        default: fontMap = fontMap1;
    }
    
    return text.split('').map(char => fontMap[char] || char).join('');
}

